public class ThreeDPoint{
  private int x;
  private int y;
  private int z;
  public ThreeDPoint(int x,int y,int z){
    this.x=x;
    this.y=y;
    this.z=z;
  }
  public int getX(){
    return x;
  }
  public int getY(){
    return y;
  }
  public int getZ(){
    return z;
  }
  public void setX(int num){
    x=num;
  }
  public void setY(int num){
    y=num;
  }
  public void setZ(int num){
    z=num;
  }
}